#include "GameScene.h"

GameScene::GameScene() {}
GameScene::~GameScene() {}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　終了関数
//////////////////////////////////////////////////////////////////////////////////////////////////

void GameScene::Finalize() {

}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　初期化関数
//////////////////////////////////////////////////////////////////////////////////////////////////

void GameScene::Init() {
	// -------------------------------------------------
	// ↓ Cameraの初期化
	// -------------------------------------------------
	mainCamera_ = std::make_unique<DebugCamera>();

	// -------------------------------------------------
	// ↓ worldObjectの初期化
	// -------------------------------------------------
	skydome_ = std::make_unique<Skydome>();
	skydome_->Init();

	// -------------------------------------------------
	// ↓ Editer初期化
	// -------------------------------------------------
	railPointEditer_ = std::make_unique<RailPointEditer>();
	railPointEditer_->Init();
}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　Resourceの読み込み
//////////////////////////////////////////////////////////////////////////////////////////////////

void GameScene::Load() {
	// worldObject
	ModelManager::LoadModel("./Game/Resources/Skydome/", "skydome.obj");
}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　更新処理
//////////////////////////////////////////////////////////////////////////////////////////////////

void GameScene::Update() {
	// -------------------------------------------------
	// ↓ Cameraの更新
	// -------------------------------------------------
	mainCamera_->Update();
	Render::SetEyePos(mainCamera_->GetWorldTranslate());
	Render::SetViewProjection(mainCamera_->GetViewMatrix(), mainCamera_->GetProjectionMatrix());

	// -------------------------------------------------
	// ↓ worldObjectの更新
	// -------------------------------------------------

	skydome_->Update();

	// -------------------------------------------------
	// ↓ GameObjectの更新
	// -------------------------------------------------

#ifdef _DEBUG
	railPointEditer_->Update();
	Debug_Gui();
#endif
}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　描画処理
//////////////////////////////////////////////////////////////////////////////////////////////////

void GameScene::Draw() const {	
	// -------------------------------------------------
	// ↓ worldObjectの描画
	// -------------------------------------------------

	// -------------------------------------------------
	// ↓ GameObjectの描画
	// -------------------------------------------------
	Engine::SetPipeline(PipelineKind::kNormalPipeline);
	skydome_->Draw();
}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　Debug表示
//////////////////////////////////////////////////////////////////////////////////////////////////

#ifdef _DEBUG
void GameScene::Debug_Gui() {
	ImGui::Begin("GameScene");

	mainCamera_->Debug_Gui();
	ImGui::End();
}
#endif